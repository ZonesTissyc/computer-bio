#!/bin/bash
# ==============================================================================
# 脚本名称: clean_ligand.sh
# 适用步骤: 三．截取配体部分 (全自动化版本)
# ==============================================================================

INPUT_PDB="4CSY.pdb"
TEMP_PDB="sLex-temp.pdb"
OUTPUT_PDB="sLex.pdb"

# 验证输入文件
if [ ! -f "$INPUT_PDB" ]; then
    echo "❌ 错误: 未在当前目录下找到 $INPUT_PDB，请确保已用 Filezilla 上传！"
    exit 1
fi

echo "🚀 开始自动化执行：三．截取配体部分..."

# ------------------------------------------------------------------------------
# 1. 截取 C 链配体部分 (对应教程第1步)
# ------------------------------------------------------------------------------
# 动态提取所有属于 C 链的异质原子(HETATM)行，避免硬编码行号带来的潜在错误
awk '$1=="HETATM" && $5=="C" {print $0}' "$INPUT_PDB" > "$TEMP_PDB"
echo "  [✓] 步骤 1 完成：已成功将 C 链配体原子提取至临时文件。"

# ------------------------------------------------------------------------------
# 2. 批量剔除各向异性 ANISOU 行 (对应教程第3步)
# ------------------------------------------------------------------------------
sed -i '/^ANISOU/d' "$TEMP_PDB"
echo "  [✓] 步骤 2 完成：已彻底滤除配体中的 ANISOU 行。"

# ------------------------------------------------------------------------------
# 3. 批量修改糖基的残基名与残基编号，并严格对齐格式 (对应教程第2步 & 第4步)
# ------------------------------------------------------------------------------
# 利用 awk 精准定位列，避免全局替换导致的坐标污染：
# - SIA (残基3) -> OSA (编号122)
# - GAL (残基2) -> 3LB (编号123)
# - MAG (残基1) -> WYB (编号124)
# - FUC (残基4) -> OfA (编号126)
# - 同时在第4步要求中：将 WYB 中原子名为 "O1" 和 "CM" 的行改写为 OME (编号125)
awk '
{
    res_name = substr($0, 18, 3);
    res_num  = int(substr($0, 23, 4));
    atom_name = substr($0, 13, 4);

    # 优先处理特殊的 OME 部分 (第4步)
    if (res_name == "MAG" && (atom_name == " O1 " || atom_name == " CM ")) {
        $0 = substr($0, 1, 17) "OME" substr($0, 21, 2) " 125" substr($0, 27);
    }
    # 处理四大基础单糖 (第2步)
    else if (res_name == "SIA") {
        $0 = substr($0, 1, 17) "OSA" substr($0, 21, 2) " 122" substr($0, 27);
    }
    else if (res_name == "GAL") {
        $0 = substr($0, 1, 17) "3LB" substr($0, 21, 2) " 123" substr($0, 27);
    }
    else if (res_name == "MAG") {
        $0 = substr($0, 1, 17) "WYB" substr($0, 21, 2) " 124" substr($0, 27);
    }
    else if (res_name == "FUC") {
        $0 = substr($0, 1, 17) "OfA" substr($0, 21, 2) " 126" substr($0, 27);
    }
    print $0;
}' "$TEMP_PDB" > temp.pdb && mv temp.pdb "$TEMP_PDB"
echo "  [✓] 步骤 3 & 4 完成：已重置所有单糖的 Residue Name 和 ID，并分离出 OME 残基。"

# ------------------------------------------------------------------------------
# 4. 按照规定顺序重组单糖并补全 TER 和 END (对应教程第5步)
# ------------------------------------------------------------------------------
> "$OUTPUT_PDB"
for i in OSA 3LB WYB OME OfA
do
    # 抽取对应残基，追加到最终配体 PDB
    grep " $i " "$TEMP_PDB" >> "$OUTPUT_PDB"
    echo "TER" >> "$OUTPUT_PDB"
done
echo "END" >> "$OUTPUT_PDB"
echo "  [✓] 步骤 5 完成：已按照 OSA -> 3LB -> WYB -> OME -> OfA 的顺序重组链并添加 TER/END。"

# ------------------------------------------------------------------------------
# 5. 精准进行原子名称的化学特化修改并保持对齐 (对应教程第6步)
# ------------------------------------------------------------------------------
# 针对特定残基，替换其对应的特定原子名。使用精确的字符位置切片和替换，防止格式错位。
awk '
{
    res_name = substr($0, 18, 3);
    atom_name = substr($0, 13, 4);

    if (res_name == "OSA") {
        if (atom_name == " C10") $0 = substr($0, 1, 12) " C5N" substr($0, 17);
        if (atom_name == " C11") $0 = substr($0, 1, 12) " CME" substr($0, 17);
        if (atom_name == " O10") $0 = substr($0, 1, 12) " O5N" substr($0, 17);
    }
    else if (res_name == "WYB") {
        if (atom_name == " C7 ") $0 = substr($0, 1, 12) " C2N" substr($0, 17);
        if (atom_name == " C8 ") $0 = substr($0, 1, 12) " CME" substr($0, 17);
        if (atom_name == " O7 ") $0 = substr($0, 1, 12) " O2N" substr($0, 17);
    }
    else if (res_name == "OME") {
        if (atom_name == " O1 ") $0 = substr($0, 1, 12) " O  " substr($0, 17);
        if (atom_name == " CM ") $0 = substr($0, 1, 12) " CH3" substr($0, 17);
    }
    print $0;
}' "$OUTPUT_PDB" > temp.pdb && mv temp.pdb "$OUTPUT_PDB"
echo "  [✓] 步骤 6 完成：特定原子名称特化更名（如 C10->C5N）全部处理完毕。"

# 清理临时文件
rm -f "$TEMP_PDB"

echo "=============================================================================="
echo "🎉 恭喜！【三．截取配体部分】已成功完美自动化！"
echo "📂 已生成结构完全合规的配体文件: $OUTPUT_PDB"
echo "=============================================================================="
