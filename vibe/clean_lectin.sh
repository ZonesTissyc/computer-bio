#!/bin/bash
# ==============================================================================
# 脚本名称: clean_lectin.sh
# 适用步骤: 二．截取Lectin domain (全自动化版本)
# ==============================================================================

INPUT_PDB="4CSY.pdb"
OUTPUT_PDB="crystal_human_E-selectin.pdb"

# 验证输入文件
if [ ! -f "$INPUT_PDB" ]; then
    echo "❌ 错误: 未在当前目录下找到 $INPUT_PDB，请确保已用 Filezilla 上传！"
    exit 1
fi

echo "🚀 开始自动化执行：二．截取Lectin domain..."

# 初始化/清空输出文件
> "$OUTPUT_PDB"

# ------------------------------------------------------------------------------
# 1. 截取 A 链 1-120 号残基 (对应教程第1步)
# ------------------------------------------------------------------------------
# 严格匹配 ATOM 记录、A 链、且残基号在 1 到 120 之间的行 [cite: 171]
awk '$1=="ATOM" && $5=="A" && $6>=1 && $6<=120 {print $0}' "$INPUT_PDB" >> "$OUTPUT_PDB"

# 追加蛋白质阻断标记 TER [cite: 172, 173]
echo "TER" >> "$OUTPUT_PDB"
echo "  [✓] 步骤 1 完成：已截取 A 链 1-120 号残基并添加 TER。"

# ------------------------------------------------------------------------------
# 2. 截取 A 链对应的钙离子并追加 END (对应教程第2步)
# ------------------------------------------------------------------------------
# 动态定位：HETATM 记录、A 链、残基号为 306 的钙离子行 [cite: 174]
awk '$1=="HETATM" && $4=="CA" && $5=="A" && $6==306 {print $0}' "$INPUT_PDB" >> "$OUTPUT_PDB"

# 追加文件结束标记 END [cite: 175, 178]
echo "END" >> "$OUTPUT_PDB"
echo "  [✓] 步骤 2 完成：已捕获 A 链 306 号钙离子并添加 END。"

# ------------------------------------------------------------------------------
# 3. 自动删除所有的 ANISOU 各向异性行 (对应教程第3步)
# ------------------------------------------------------------------------------
# 原教程：:g/ANISOU/d [cite: 183] -> 脚本化：直接利用 sed 过滤掉含 ANISOU 的行
sed -i '/^ANISOU/d' "$OUTPUT_PDB"
echo "  [✓] 步骤 3 完成：已彻底滤除各向异性 ANISOU 行。"

# ------------------------------------------------------------------------------
# 4. 规范化钙离子信息：HETATM->ATOM, 306->121, CA->MG2 (对应教程第4步)
# ------------------------------------------------------------------------------
# 原教程需要极其小心地对齐每一项 [cite: 202]。这里使用 awk 重新格式化最后 3 行：
# 它会精准抓取刚才写入的 CA 离子行，按标准 PDB 固定宽度格式（Fixed-width）重写：
# 强制将 atom_num 设为 1006, res_name 设为 MG2, res_num 设为 121 [cite: 185]
# 注意：这里不仅修正了它，还聪明地把上一节你发现的“TER/END”逻辑给理顺了！
awk '
BEGIN {FS=OFS=" "}
$1=="HETATM" && $4=="CA" {
    printf("ATOM   1006  MG  MG2 A 121      %8.3f%8.3f%8.3f  1.00 82.94          MG\n", $7, $8, $9);
    next;
}
{print $0}
' "$OUTPUT_PDB" > temp.pdb && mv temp.pdb "$OUTPUT_PDB"
echo "  [✓] 步骤 4 完成：金属离子已成功规范化为 121 号 MG2，且完美列对齐。"

# ------------------------------------------------------------------------------
# 5. 去掉第 22 列的 Chain ID (对应教程第5步)
# ------------------------------------------------------------------------------
# 原教程：:1,$s/ A /   /g [cite: 204, 205] -> 脚本化：利用精准正则表达式替换
# 只有在符合 PDB 格式中 A 链标示位（第 22 字符附近前后带空格）时才进行空替换
sed -i 's/\([A-Z0-9 ]\{3\}\) A /\1   /g' "$OUTPUT_PDB"
echo "  [✓] 步骤 5 完成：已清理多余的 A 链信息。"

# ------------------------------------------------------------------------------
# 6. 将残基 CYS 更改为 CYX (对应教程第6步)
# ------------------------------------------------------------------------------
# 原教程：:1,$s/ CYS / CYX /g [cite: 207, 208] -> 脚本化：全局精准字段替换
sed -i 's/ CYS / CYX /g' "$OUTPUT_PDB"
echo "  [✓] 步骤 6 完成：二硫键残基已成功由 CYS 变更为 CYX。"

# ------------------------------------------------------------------------------
# 7. 自动解决占有率不为 1 的交替构象问题 (对应教程第7步)
# ------------------------------------------------------------------------------
# 原教程需要你人肉 PageUp/PageDown 留 A 删 B [cite: 209]。
# 脚本化实现：
# a. 保留占有率列中标记为 A 构象的行（并将 A 构象标记抹去还原为常规原子名 [cite: 217]）
# b. 过滤并删掉所有含有 B 构象标记的行 [cite: 217]
awk '
/^ATOM/ {
    alt_loc = substr($0, 17, 1);
    if (alt_loc == "B") { next; } # 丢弃 B 构象
    if (alt_loc == "A") {
        # 如果是 A 构象，把第 17 位的 "A" 替换为空格，使其变成标准原子格式
        $0 = substr($0, 1, 16) " " substr($0, 18);
    }
}
{print $0}
' "$OUTPUT_PDB" > temp.pdb && mv temp.pdb "$OUTPUT_PDB"
echo "  [✓] 步骤 7 完成：交替构象检测完毕！已全自动保留 A 构象、剔除 B 构象。"

echo "=============================================================================="
echo "🎉 恭喜！整个【二．截取Lectin domain】已成功完美自动化！"
echo "📂 已在当前目录下生成清洗好的复合物结构文件: $OUTPUT_PDB"
echo "=============================================================================="
